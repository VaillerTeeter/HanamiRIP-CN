/*
	这个文件是 Bangumi 相关服务的“模块入口”。
	作用：把这一组功能拆成多个小文件，再在这里统一声明。
	这样做的好处：
	1) 代码分工清晰：每个文件只做一类事情。
	2) 便于维护：要找功能只看对应模块。
	3) 编译可控：Rust 需要明确声明模块，才会把文件编译进来。
*/

// 声明 `api` 子模块：
// 这里通常放“对外接口调用/请求逻辑”，比如请求 Bangumi 的网络 API。
// 注意：`mod api;` 表示这个模块只在本模块内部使用，不对外暴露。
mod api;
// 声明并公开 `commands` 子模块：
// `pub` 的意思是“外部也能用”，常用于暴露给其他模块或前端调用的命令入口。
pub mod commands;
// 声明 `filters` 子模块：
// 通常放数据过滤、条件筛选的逻辑（如按类型/年份/关键字过滤）。
mod filters;
// 声明 `models` 子模块：
// 放数据结构定义（结构体/枚举等），描述 Bangumi 相关的“数据长什么样”。
mod models;
// 声明 `translate` 子模块：
// 通常负责字段映射/文本转换，比如把外部数据转成内部需要的格式。
mod translate;
